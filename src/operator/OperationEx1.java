package operator;

public class OperationEx1 {
    public static void main(String[] args) {

        //세수의 합
        int num1 = 10;
        int num2 = 20;
        int num3 = 30;
        int sum = (num1 + num2 + num3);
        System.out.println("합계 = " + sum);
        //세 변수의 평균
        int average = sum / 3;
        System.out.println("평균 = " + average);
    }
}
