package variable;

public class Var5 {
    public static void main(String[] args) {

        int a;
        a = 1; // 초기화 아무것도 없는 변수에 값을 넣는 것!
        System.out.println(a);

        int b = 2; // 변수를 선언하면서 초기화하며 값을 넣는 것

        int c = 3, d = 4;
        System.out.println(c);
        System.out.println(d);

        // 변수는 꼭 초기화를 해야한다.
    }

}
