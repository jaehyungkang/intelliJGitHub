package variable;

public class Var7 {
    public static void main(String[] args) {
        int a = 200; // 정수를 입력가능하다
        double b = 10.5; // 실수 입력
        boolean c = true; // 불린 true, false 입력가능함
        char d = 'A'; // 문자 하나만 넣을 수 있음
        String e = "Hello jaehyung"; // 문자열을 넣을 수 있음

        // 내용물을 담을 규격이 다정해져 있음....!!
        // 변수는 데이터를 다룰 수 있는 형식에 따라서 규격이 다 존재한다.


        System.out.println(a);
        System.out.println(b);
        System.out.println(c);
        System.out.println(d);
        System.out.println(e);

    }
}
